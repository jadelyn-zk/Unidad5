package CuentasBancarias;

public class Transaccion {
	
	int dia;
	int mes;
	int año;
	String concepto;
	double importe;
	
	public Transaccion(int dia, int mes, int año, String concepto, double importe) {
		super();
		this.dia = dia;
		this.mes = mes;
		this.año = año;
		this.concepto = concepto;
		this.importe = importe;
	}
	
	

	
	
}
