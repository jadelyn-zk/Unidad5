package CuentasBancarias;

public class Titular {
	String dni;
	String nombre;
	String apellido;
	int telefono;
	
	public Titular(String dni, String nombre, String apellido, int telefono) {
		super();
		this.dni = dni;
		this.nombre = nombre;
		this.apellido = apellido;
		this.telefono = telefono;
	}
	
	
	
}
